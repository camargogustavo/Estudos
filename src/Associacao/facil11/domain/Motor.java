package Associacao.facil11.domain;

public class Motor {
    private String nome;
    private int cavalos;

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setCavalos(int cavalos) {
        this.cavalos = cavalos;
    }

    public String getNome() {
        return nome;
    }

    public int getCavalos() {
        return cavalos;
    }
}
